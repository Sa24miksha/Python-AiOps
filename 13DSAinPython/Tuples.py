# Ordered collection, immutable (cannot change directly).
# Use ( ) brackets.


colors = ("red", "green", "blue")

print(colors[0])      # red
print(len(colors))    # 3
print(colors + ("yellow",))  # concatenate

t = (1, 2, 2, 3)

print(t.count(2))     # 2 → occurrences
print(t.index(3))     # 3 → index




